// gen-test-suites generates per-RPC TestSuite proto files from existing
// service definitions. For every service+method declared in proto/api/**, it
// emits one .proto file per service to proto/testing/cases/<api-path>/, with
// a typed TestSuite message per method. Run via `make proto`.
package main

import (
	"fmt"
	"os"
	"path/filepath"
	"regexp"
	"sort"
	"strings"
)

var (
	packageRe = regexp.MustCompile(`(?m)^\s*package\s+([\w.]+)\s*;`)
	serviceRe = regexp.MustCompile(`(?m)^\s*service\s+(\w+)\s*\{`)
	rpcRe     = regexp.MustCompile(`(?m)^\s*rpc\s+(\w+)\s*\(\s*(\w+)\s*\)\s*returns\s*\(\s*(\w+)\s*\)`)
)

type rpcDef struct{ name, input, output string }
type serviceDef struct {
	pkg     string
	name    string
	srcFile string
	rpcs    []rpcDef
}

func main() {
	if len(os.Args) != 3 {
		fmt.Fprintln(os.Stderr, "usage: gen-test-suites <proto-root> <output-root>")
		os.Exit(2)
	}
	protoRoot, outRoot := os.Args[1], os.Args[2]

	services, err := scan(filepath.Join(protoRoot, "api"))
	if err != nil {
		fail("scan: %v", err)
	}
	if len(services) == 0 {
		fail("no services found under %s/api", protoRoot)
	}

	for _, s := range services {
		path := writePath(outRoot, s)
		if err := os.MkdirAll(filepath.Dir(path), 0o755); err != nil {
			fail("mkdir: %v", err)
		}
		if err := os.WriteFile(path, render(s), 0o644); err != nil {
			fail("write %s: %v", path, err)
		}
		fmt.Printf("wrote %s\n", path)
	}
}

func fail(format string, a ...any) {
	fmt.Fprintf(os.Stderr, format+"\n", a...)
	os.Exit(1)
}

func scan(dir string) ([]serviceDef, error) {
	var out []serviceDef
	err := filepath.WalkDir(dir, func(path string, d os.DirEntry, err error) error {
		if err != nil {
			return err
		}
		if d.IsDir() || !strings.HasSuffix(path, ".proto") {
			return nil
		}
		data, err := os.ReadFile(path)
		if err != nil {
			return err
		}
		out = append(out, parse(string(data), path)...)
		return nil
	})
	if err != nil {
		return nil, err
	}
	sort.Slice(out, func(i, j int) bool { return out[i].name < out[j].name })
	return out, nil
}

func parse(src, srcFile string) []serviceDef {
	pkg := ""
	if m := packageRe.FindStringSubmatch(src); len(m) == 2 {
		pkg = m[1]
	}
	var services []serviceDef
	rest := src
	for {
		loc := serviceRe.FindStringSubmatchIndex(rest)
		if loc == nil {
			return services
		}
		name := rest[loc[2]:loc[3]]
		body, after := extractBlock(rest[loc[1]-1:])
		s := serviceDef{pkg: pkg, name: name, srcFile: srcFile}
		for _, m := range rpcRe.FindAllStringSubmatch(body, -1) {
			s.rpcs = append(s.rpcs, rpcDef{name: m[1], input: m[2], output: m[3]})
		}
		services = append(services, s)
		rest = after
	}
}

func extractBlock(s string) (string, string) {
	open := strings.Index(s, "{")
	if open < 0 {
		return "", ""
	}
	depth := 0
	for i := open; i < len(s); i++ {
		switch s[i] {
		case '{':
			depth++
		case '}':
			depth--
			if depth == 0 {
				return s[open+1 : i], s[i+1:]
			}
		}
	}
	return s[open+1:], ""
}

// writePath gives the output .proto location for a service.
func writePath(outRoot string, s serviceDef) string {
	// pkg "api.v1" → "api/v1/"
	pkgPath := filepath.Join(strings.Split(s.pkg, ".")...)
	// One subdir per service so each generates into its own Go package.
	return filepath.Join(outRoot, pkgPath, snake(s.name), "cases.proto")
}

// render produces the contents of the per-service cases proto file.
func render(s serviceDef) []byte {
	subPkg := snake(s.name)
	pkg := s.pkg + "." + subPkg
	apiImport := apiImportPath(s.srcFile)
	goPkgDir := strings.ReplaceAll(s.pkg, ".", "/") + "/" + subPkg
	goPkgAlias := strings.ReplaceAll(subPkg, "_", "") + "casesv1"

	var b strings.Builder
	fmt.Fprintf(&b, "// Code generated by gen-test-suites. DO NOT EDIT.\n")
	fmt.Fprintf(&b, "syntax = \"proto3\";\n\n")
	fmt.Fprintf(&b, "package testing.cases.%s;\n\n", pkg)
	fmt.Fprintf(&b, "import \"%s\";\n", apiImport)
	fmt.Fprintf(&b, "import \"testing/v1/test_case.proto\";\n\n")
	fmt.Fprintf(&b, "option go_package = \"github.com/at-ishikawa/langner/gen-protos/testing/cases/%s;%s\";\n\n",
		goPkgDir, goPkgAlias)

	for _, r := range s.rpcs {
		fmt.Fprintf(&b, "message %sTestSuite {\n", r.name)
		fmt.Fprintf(&b, "  repeated %sTestCase cases = 1;\n", r.name)
		fmt.Fprintf(&b, "}\n\n")

		fmt.Fprintf(&b, "message %sTestCase {\n", r.name)
		fmt.Fprintf(&b, "  string name = 1;\n")
		fmt.Fprintf(&b, "  repeated testing.v1.SetupStep setup = 2;\n")
		fmt.Fprintf(&b, "  .%s.%s request = 3;\n", s.pkg, r.input)
		fmt.Fprintf(&b, "  %sExpected expected = 4;\n", r.name)
		fmt.Fprintf(&b, "}\n\n")

		fmt.Fprintf(&b, "message %sExpected {\n", r.name)
		fmt.Fprintf(&b, "  string status = 1;            // \"\", \"OK\", \"INVALID_ARGUMENT\", \"NOT_FOUND\", ...\n")
		fmt.Fprintf(&b, "  .%s.%s body = 2;\n", s.pkg, r.output)
		fmt.Fprintf(&b, "  testing.v1.ExpectedError error = 3;\n")
		fmt.Fprintf(&b, "  repeated testing.v1.StateAssertion state = 4;\n")
		fmt.Fprintf(&b, "}\n\n")
	}
	return []byte(b.String())
}

// apiImportPath converts an absolute or workspace-relative path like
// proto/api/v1/notebook.proto into the buf import path "api/v1/notebook.proto".
func apiImportPath(srcFile string) string {
	idx := strings.Index(srcFile, "/proto/")
	if idx == -1 {
		// Already a relative path under proto/; strip leading "proto/" if present.
		return strings.TrimPrefix(filepath.ToSlash(srcFile), "proto/")
	}
	return filepath.ToSlash(srcFile[idx+len("/proto/"):])
}

// snake converts CamelCase to snake_case.
func snake(s string) string {
	var b strings.Builder
	for i, r := range s {
		if i > 0 && r >= 'A' && r <= 'Z' {
			b.WriteByte('_')
		}
		if r >= 'A' && r <= 'Z' {
			r = r + ('a' - 'A')
		}
		b.WriteRune(r)
	}
	return b.String()
}
